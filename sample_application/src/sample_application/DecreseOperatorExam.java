package sample_application;

public class DecreseOperatorExam {

	public static void main(String[] args) {
		
		
		int number1 = 0;
		number1 = number1 -1; 
		
		number1 =0;
		number1 -=1;
		
		number1 =0;
		number1--;
		
		System.out.println(number1--);
		System.out.println(number1--);
		System.out.println(number1--);
		System.out.println(number1--);
	}
}
